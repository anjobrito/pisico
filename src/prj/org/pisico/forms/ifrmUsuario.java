    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.usuario.getIdUsuario() == null) {
            TbUsuario newUsuario = new TbUsuario();
            newUsuario.setStrNome(this.jTextField2.getText());
            newUsuario.setStrUsuario(this.jTextField3.getText());
            newUsuario.setStrPwd(this.jPasswordField1.getText());
            this.usuario.setTbPerfilHasTbUsuarioCollection(null);
            newUsuario.setDtDataEntrada(new Date());
            new usuarioController().saveOrUpdate(newUsuario);

        } else {
            this.usuario.setStrNome(this.jTextField2.getText());
            this.usuario.setStrUsuario(this.jTextField3.getText());
            this.usuario.setStrPwd(this.jPasswordField1.getText());
            this.usuario.setDtDataEntrada(new Date());
            this.usuario.setTbPerfilHasTbUsuarioCollection(null);
            new pisico_dao().saveOrUpdate(this.usuario);
        }
        this.limpaCampos();
        this.jTable1.setModel(new usuarioController().grid());
        this.jTabbedPane1.setSelectedIndex(0);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.usuario != null) {
            this.usuario.setTbPerfilHasTbUsuarioCollection(null);            
            new usuarioController().delete(this.usuario);
            this.limpaCampos();
            this.jTable1.setModel(new usuarioController().grid());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

