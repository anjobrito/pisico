    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
      if(evt.getClickCount()==2){
        this.usuario = new TbUsuario();
        this.usuario = (TbUsuario) this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0);
        this.jTextField2.setText(this.usuario.getStrNome());
        this.jTextField3.setText(this.usuario.getStrUsuario());
        this.jPasswordField1.setText(this.usuario.getStrPwd());
        this.jTabbedPane1.setSelectedIndex(1);
      }
    }//GEN-LAST:event_jTable1MouseClicked

