    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.convenio.getIdConvenio() == null) {
            TbConvenio newConvenio = new TbConvenio();
            newConvenio.setStrDescricao(this.jTextField3.getText());
            newConvenio.setStrStatus(this.getTrocaStatus((String) this.jComboBox2.getSelectedItem()));
            newConvenio.setStrValorHora(Double.parseDouble(this.jFormattedTextField1.getText().replace(",", ".")));
            newConvenio.setTbAgendaCollection(null);
            newConvenio.setTbPacienteCollection(null);
            new convenioController().saveOrUpdate(newConvenio);
        } else {
            this.convenio.setStrDescricao(this.jTextField3.getText());
            this.convenio.setStrStatus(this.getTrocaStatus((String) this.jComboBox2.getSelectedItem()));
            this.convenio.setStrValorHora(Double.parseDouble(this.jFormattedTextField1.getText().replace(",", ".")));
            this.convenio.setTbAgendaCollection(null);
            this.convenio.setTbPacienteCollection(null);
            new pisico_dao().saveOrUpdate(this.convenio);
        }
        this.limpaCampos();
        this.jTabbedPane1.setSelectedIndex(0);

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.convenio.getIdConvenio() != null) {
            this.convenio.setTbAgendaCollection(null);
            this.convenio.setTbPacienteCollection(null);
            new convenioController().delete(this.convenio);
            this.limpaCampos();
            this.jTable1.setModel(new convenioController().grid());
            this.jComboBox2.setSelectedItem("...");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

