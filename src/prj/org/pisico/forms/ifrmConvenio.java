    private String getTrocaStatus(String value) {
        String status = "";
        if (value.equals("...")) {
            status = "I";
        } else if (value.equals("Ativo")) {
            status = "A";
        } else if (value.equals("Inativo")) {
            status = "I";
        }
        return status;
    }

    private String setTrocaStatus(String value) {
        String status = "";
        if (value.equals("")) {
            status = "...";
        } else if (value.equals("A")) {
            status = "Ativo";
        } else if (value.equals("I")) {
            status = "Inativo";
        } else {
            status = "I";
        }
        return status;
    }

