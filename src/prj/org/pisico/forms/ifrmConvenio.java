    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            this.convenio = new TbConvenio();
            this.convenio = (TbConvenio) this.jTable1.getValueAt(this.jTable1.getSelectedRow(), 0);
            this.jTextField3.setText(this.convenio.getStrDescricao());
            this.jComboBox2.setSelectedItem(this.getTrocaStatus(this.convenio.getStrStatus()));
            this.jFormattedTextField1.setText(String.valueOf(this.convenio.getStrValorHora()).replace(".", ","));
            this.jTabbedPane1.setSelectedIndex(1);
        }

    }//GEN-LAST:event_jTable1MouseClicked

