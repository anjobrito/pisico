    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (this.jTable2.isRowSelected(this.jTable2.getSelectedRow())) {
            this.agenda = (TbAgenda) this.jTable2.getValueAt(this.jTable2.getSelectedRow(), 0);
            ifrmatendimento = new ifrmAtendimento(JDesktopPanel);
            ifrmatendimento.setTitle(this.agenda.getIdPaciente().getStrNome() + " - " + new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").format(this.agenda.getDtAgendamento()));
            ifrmatendimento.setPosicao();
            this.ifrmatendimento.setAgenda(this.agenda);
            this.JDesktopPanel.add(this.ifrmatendimento);
            this.ifrmatendimento.show();
            this.limpaCampos();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

