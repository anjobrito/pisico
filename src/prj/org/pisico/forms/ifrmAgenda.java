    public void limpaCampos() {
        this.jComboBox1.setModel(new pacienteController().comboBox());
        this.jLabel1.setText("----------------");
        this.jFormattedTextField1.setText(new SimpleDateFormat("hh:mm").format(new Date()));
        try {
            this.jTable2.setModel(new agendaController().grid(strDataHora));
        } catch (ParseException ex) {
            Logger.getLogger(ifrmAgenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.agenda = new TbAgenda();
        this.strDataHora = new SimpleDateFormat("dd/MM/yyyy").format(this.dateChooserPanel2.getSelectedDate().getTime());
        try {
            this.jTable2.setModel(new agendaController().grid(strDataHora));
        } catch (Exception ex) {
            Logger.getLogger(ifrmAgenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.jButton5.setEnabled(false);
        this.jTable2.clearSelection();
    }
    
