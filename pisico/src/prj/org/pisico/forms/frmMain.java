    private void analisaTelaIncial(TbUsuario auxUsuario) {
        int NivelAcesso = 0;
        for (Iterator i = auxUsuario.getTbPerfilHasTbUsuarioCollection().iterator(); i.hasNext();) {
            TbPerfilHasTbUsuario perfilhasUsuario = (TbPerfilHasTbUsuario) i.next();
            TbPerfil perfil = perfilhasUsuario.getTbPerfilIdPerfil();
            NivelAcesso = perfil.getIntNive();
        }

        switch (NivelAcesso) {
            case 1: {

                break;
            }
            case 2: {
                this.jMenu5.setVisible(false);
                this.jMenu6.setVisible(false);
                this.jMenuItem10.setVisible(false);

                break;
            }

            case 3: {
                this.jMenu1.setVisible(false);
                this.jMenu3.setVisible(false);
                this.jMenuItem12.setVisible(false);
                this.jMenuItem12.setVisible(false);
                this.jMenuItem9.setVisible(false);
                break;
            }
            default: {
                this.jMenu1.setVisible(false);
                this.jMenu2.setVisible(false);
                this.jMenu3.setVisible(false);
                JOptionPane.showMessageDialog(this, "Usu√°rio sem perfil atribuido, Entre em contato com o Administrador do sistems.");
            }

        }
    }

